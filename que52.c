#include <stdio.h>

int main() {

    printf("*\n");        // 1 star

    printf("\n");         // blank line

    printf("*\n*\n*\n");  // 3 stars

    printf("\n");         // blank line

    printf("*\n*\n*\n*\n*\n"); // 5 stars

    printf("\n");         // blank line

    printf("*\n*\n*\n");  // 3 stars

    printf("\n");         // blank line

    printf("*\n");        // 1 star

    return 0;
}
